# Integer variable
num1 = 10
num2 = 5

#Integer Division

result1 = num1 // num2 
print("Integer Division:", result1)     # Will give you only the whole number and throw away the decimal.

result2 = num1 % num2     # Returns remainder after division
print("Modulus (remainder):", result2)

result3 = abs(-10)          # Returns postive number (abs = absolute)
print("Absolute value:", result3)