#Small Math
num1 = 5
num2 = 2.5

#addition
sum_result = num1 + num2
print("Addition Result:", sum_result)

#subtraction
sub_result = num1 - num2
print("Result of substraction:", sub_result)

#Multiplication
multi_result = num1 * num2
print("Result of Multiplication:", multi_result)

#Division
div_result = num1 / num2 
print("Result of division:", div_result)

#Approximation
appr_result = round(5.784732398077, 2)
print("Rounded to 2 decimal places result:", appr_result)

